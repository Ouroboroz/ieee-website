				$("#form_alert").html("Please enter your first name.");
				pass = false;
			}
			if ($("#form_last_name").val() == ""){
				$("#form_alert").html($("#form_alert").html()+"<br>Please enter your last name.");
				pass = false;
			}
			if ($("#form_email").val() == ""){
				$("#form_alert").html($("#form_alert").html()+"<br>Please enter your email.");
				pass = false;
			}
			if (!pass) {
				return;
			}
			$.ajax({
			url : 'listserv.php',
			type : 'post',
			data : {
			first_name : $("#form_first_name").val(),
			last_name : $("#form_last_name").val(),
			email : $("#form_email").val()
			},
			success : function( response ) {
				$("#form_alert").html(response)
			}
			});
