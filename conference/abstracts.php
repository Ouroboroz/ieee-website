		<p class="title"><b>Wireless Ultrasonic Leak Detection for International Space Station -</b> <i class="name">Professor Ali Abedi</i></p>
		<p>

Air leaks in pressurized structures such as space vehicles or habitats due to micrometeorite

impacts or structural aging and failure are inevitable. Due to the large pressure differential

between interior of the module and space outside, the escaping air emits an ultrasonic wave

inside the module that may be detected using ultrasonic sensors. This presentation describes

hardware, software, and algorithmic challenges for localizing leaks using an array of ultrasonic

sensors. A major challenge with leak localization is inaccuracy due to reflections and noise.

With the use of Kalman filtering, tree-search algorithm, and a hyperbolic angle of arrival

algorithm, we can eliminate many of these inaccuracies. Design considerations for a standalone

device with its own battery power source, on-board processing, crew interfaces, signal-

conditioning circuitry, and a custom 3-D printed box are discussed in this presentation. Three

devices were built, flight certified and scheduled to deploy to the ISS for data collection during

the Fall of 2016. This project was funded through a three year NASA EPSCoR grant in

collaboration with the Office of ISS Technology Demonstration Programs.</p>
