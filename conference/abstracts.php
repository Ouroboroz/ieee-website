 Scientific research is hugely important -- it's where we discover the answers to previously unanswered questions, how we build new technologies, and in general how we make progress in our field.  But not everyone who does research as a undergraduate or graduate student ends up with a job in research.  What good is doing research if you don't pursue it as a career?  In this talk, Dr. LaCurts will cover her journey through research -- which sometimes went very well, and sometimes was a disaster -- and explain how she has been able to use her research skills in her decidedly non-research-oriented teaching career.</p>
