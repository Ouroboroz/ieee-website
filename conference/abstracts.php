		<div id="giakos" class="col-10">
		<p class="title"><b>Agile Unmanned Vehicles (Drones) and Bioinspired Vision Sensors -</b> <i class="name">Professor George Giakos</i></p>
		<p>
The goal of this presentation is to introduce the technical challenges associated with the design and operational capabilities of autonomous navigation vision systems, then introduce sensing principles and techniques, borrowed from biological sensory response systems, overcoming these challenges. Computer vision has a center role in unmanned aerial vehicles, for a number of tasks like s-localization, navigation, target recognition and tracking. A major challenge in the design of autonomous navigtaion systems is the processing of the visual information. As a result, the operation of such systems in urban settings is limited by their competing design requirements leading to reduced agility. The above challenges can be mitigated by means of bioinspired vision principles. Bioinspired computer vision is promising towards building adaptive computer vision systems with enhanced agility. Invertebrates, such as insects, as well as some crustaceans, such as the mantis shrimp, have compound eyes. The advantages of the compound eyes consist on their superb wide viewing angle and the fast movement tracking due to the large amount of photoreceptor units that do not have to individually move to track, and the ability to detect polarized light. In addition, certain animals, exhibit unique navigation capabilities, because of their homing behaviors, i.e., their ability to return home by using visual ques. The enhanced polarization vision capabilities of certain animals, combined with a wide field-of- view, fast tracking, multi-functional eye structure, and homing, add agility to the design of drones, while providing enhanced structural, localization, navigation, and tracking capabilities. As result, bioinspired vision principles can be applied not only towards the design of autonomous navigation systems, but also to the design of efficient biomedical systems for early diagnosis of cancer, and enhanced detection and identification of tissue pathologies, because of their polarization sensitivity. 
</p>
